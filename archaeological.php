 <h1 style="color:red;">Wari-Bateshwar</h1>
 <img src="assets/images/wari-batesshor.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Wari-Bateshwar an archaeological site in Narsinghdi district of Bangladesh. Cultivation of land, digging drains, cutting garbage-pit and red soil for building local traditional mud-house and other household activities of the local people regularly turn the surface upside down which expose the objects of ancient people in Wari-Bateshwar. In addition, after rain fall, wonderful beads of semi-precious stone and glass, silver punch-marked coins etc. are rinsed out which results into clear visibility. In 1930s, local schoolteacher Hanif Pathan and afterward his son Habibulla Pathan started collecting those artifacts and later carried out research with a curious mind. But for a long time, this potentially important archaeological site had failed to attract the attention of professional archaeologists in Bangladesh. After waiting for 60 years, archaeological exploration started in 1989 resulting regular excavation from 2000.</p>
          
          
 
