 <h1 style="color:red;">Panam Nagar</h1>
 <img src="assets/images/panam.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Located close to Dhaka in Bangladesh, the ghost town of Panam City, or Panam Nagar, lies within Sonargaon, a former Hindu settlement dating to the early 13th century. Later that period, a medieval philosopher called Sharfuddin Abu Tawwamah established the area as an Islamic academic centre, building a Madrasa and Khanqah, a spiritual retreat for Sufi religious gatherings.Sonargaon benefited from much seafaring trade from the 14th century onwards. Its historic importance is reflected in its strategic location at the end of the Grand Trunk Road and the striking Indo-European architecture of Panam Nagar’s decaying buildings.Fuelled by British colonial rule and a prosperous cotton textile industry, Panam City became home to upper-middle class Bengali businessmen in the late 1800s.During that time, Ananda Mohan Piddar House was built along with many grand town houses which now give the ‘Lost City’ its ghost town appearance.Most of these beautiful structures fell into disrepair following the end of British rule in the mid 20th century, and the once affluent area was virtually abandoned following the Second Kashmir War in 1965.In 2006, the World Monuments Fund (WMF) added ‘Sonargaon-Panam City’ to their Watch List of 100 endangered sites and listed the site again in 2008 (along with St Peter’s Seminary in Scotland).Although the WMF notes that Bangladesh’s Department of Archaeology has carried out all the restoration work it can afford to do, most buildings continue to deteriorate due to damp and woodworm. Greater access to the historic city has been called for in order to attract tourists to the area and raise awareness.Three brick bridges from the Mughal period still exist today, as do several canals which were dug in the city for protection. One of these provides the waterfront of a folk art and craft museum called Lok Shilpa Jadughar, which was opened in 1975 by Joynul Abedin, a Bangladeshi painter, within an original 1901 house named Bara Sardar Bari.</p>
          
          
 
 <h3 style="color:green;">How to Go</h3>
  <p align="justify">Panam City is situated at Sonargaon, Narayngang in Bangladesh. It covered 20 km area of Sonargaon.
</p>

