 <h1 style="color:red;">Paharpur Buddha Bihar</h1>
 <img src="assets/images/paharpur.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Geographically located to the north-west of Bangladesh in the district of Naogaon, the heart-land of ancient “Varendra”, close to the village of Paharpur the extensive ruins of the Buddhist monastic complex are the most spectacular and important pre-Islamic monument in Bangladesh.
The first builder of the monastery was Dharmapala Vikramshila (770-810AD), the king of Varendri-Magadha, as inscribed on a clay seal discovered in the monastery compound. The plan of the monastery can be described as a large square quadrangle measuring approximately 920 feet, with the main entrance, an elaborate structure, on the northern side. The outer walls of the monastery are formed by rows of cells that face inwards toward the main shrine in the centre of the courtyard. In the last building phases of the Monastery these cells, which formed the outer wall, totalled 177. The main central shrine has a cruciform ground plan and a terraced superstructure that rises in three terraces above ground level to a height of about 70 feet. The upper level is a massive rectangular central block which forms the central brick shaft. The intermediate terrace is a wide circumambulatory path which passes four main chapels or  mandapas architectural plan, it is in fact a simple cruciform that has been elaborated with a series of projections at the re-entrants, a form that is copied at all levels on the main shrine. At the intermediate level there were originally two bands of terracotta plaques running around the full perimeter of the shrine, out of which half are still preserved in situ.</p>
          
          
 
 <h3 style="color:green;">How to Go</h3>
  <p align="justify">Paharpur Buddha Bihar in Paharpur, Badalgachhi Upazila, Naogaon District
</p>

