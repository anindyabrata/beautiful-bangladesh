<h3 style="color:green;">Black rice</h3>
  <p align="justify">Black rice (also known as purple rice) is a range of rice types of the species Oryza sativa L., some of which are glutinous rice. Varieties include Indonesian black rice and Thai jasmine black rice. Black rice is known as Chak-hao in Manipur, an Indian state in the eastern border. Pudding made of Chak-hao is a popular dessert served to people at all major feasts in Manipur. Black rice is high in nutritional value and is a source of iron, vitamin E, and antioxidants (more than in blueberries). The bran hull (outermost layer) of black rice contains one of the highest levels of anthocyanin antioxidants found in food. The grain has a similar amount of fiber to brown rice and, like brown rice, has a mild, nutty taste.
</p>

          
                 
       
          
