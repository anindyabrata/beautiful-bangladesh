  <p align="justify">Brown rice is whole grain rice, with the inedible outer hull removed; white rice is the same grain with the hull, bran layer and cereal germ removed. Red rice, gold rice, black rice and purple rice are all whole rices, but with a differently-pigmented outer layer.
Any type of rice may be eaten whole. Whole rice has a mild, nutty flavor, and is chewier and more nutritious than white rice.
</p>

          
           
          
