<img src="assets/images/luchi.jpg" width="800px" height="400px" /> <br> <br>
<h3 style="color:green;">Luchi</h3>
  <p align="justify">Luchi is a deep-fried flatbread made of wheat flour that is typical of Bengali, Assamese, Maithili and Oriya cuisine. In order to make luchis, a dough is prepared by mixing fine maida flour with water and a spoonful of ghee, which is then divided into small balls. These balls are flattened using a rolling-pin and individually deep-fried in cooking oil or ghee. A typical luchi will measure 4-5 inches in diameter. They are usually served with curries or gravies. If maida is substituted with atta, it is called a poori. Luchi that is stuffed is called kochuri; kochuri stuffed with mashed peas (koraishutir kochuri) is one notable variety.
</p>

                                         
          
<img src="assets/images/machherjhol.jpg" width="800px" height="400px" /> <br> <br>
<h3 style="color:green;">Machher Jhol</h3>
  <p align="justify">Machher Jhol or Machha Jhola is a traditional Bengali and Odia spicy fish stew. It is in the form of a very spicy stew that is served with rice. Machher Jhol is liberally seasoned with turmeric, garlic, onions, and grated ginger. Potatoes are added to the stew as a thickening agent. Tomatoes are also added to impart the dish with a reddish color which is preferred by the people of Bengal.

The kinds of fish that typically used in Bengali and Oriya households are Hilsa (called Ilisi), Rohu (called Rohi), and Catla (called Bhakura). Apart from these, there are some famous small sized fish that are normally favoured over others.
</p>

                                            
                           
                            
          
          
                                             
