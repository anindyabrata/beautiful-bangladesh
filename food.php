<h3 style="color:green;">Dal</h3>
  <p align="justify">Dal (also spelled daal or dhal; pronunciation: [d̪aːl]) is a term in the Indian subcontinent for dried, split pulses (that is, lentils, peas, and beans). The term is also used for various soups prepared from these pulses. These pulses are among the most important staple foods in SAARC countries, and form an important part of Indian, Nepalese, Pakistani, Sri Lankan and Bangladeshi cuisines.

Dals are frequently eaten with flatbreads such as rottas or chapatis or with rice, a combination referred to as dal bhat. Dals are high in protein relative to other plants.

The outer hull is usually stripped off; dal that has not been hulled is described as chilka (skin), e.g. chilka urad dal, mung dal chilka in Hindi/Urdu. The term dal is often contrasted with the term gram, used in South Asia for pulses that are whole rather than split.
</p>

                                   
                       
          
