 <h1>আমার সোনার বাংলা (Bangla)</h1>

     <p align="justify">আমার সোনার বাংলা,<br>
আমি তোমায় ভালবাসি।<br>
চিরদিন তোমার আকাশ,<br>
তোমার বাতাস<br>
আমার প্রাণে বাজায় বাঁশি।<br>

ও মা,<br>
ফাগুনে তোর আমের বনে<br>
ঘ্রাণে পাগল করে<br>
মরি হায়, হায় রে<br>
ও মা,<br>
অঘ্রানে তোর ভরা ক্ষেতে,<br>
আমি কী দেখেছি মধুর হাসি।।<br>

কী শোভা, কী ছায়া গো,<br>
কী স্নেহ, কী মায়া গো,<br>
কী আঁচল বিছায়েছ<br>
বটের মূলে,<br>
নদীর কূলে কূলে।<br>

মা, তোর মুখের বাণী<br>
আমার কানে লাগে<br>
সুধার মতো-<br>

মা তোর বদন খানি মলিন হলে<br>
আমি নয়ন<br>
ও মা আমি নয়ন জলে ভাসি<br>
সোনার বাংলা,<br>
আমি তোমায় ভালবাসি।<br>
         
<h1>My beloved Bengal (Literal English Translation)</h1>
     
     <p align="justify">
         My Bengal of Gold,<br>
I love you.<br>

Forever your skies,<br>
Your air set my heart in tune<br>
As if it were a flute.<br>

In spring, O mother mine,<br>
The fragrance from your mango groves<br>
Makes me wild with joy,<br>
Ah, what a thrill!<br>
In autumn, O mother mine,<br>
In the full blossomed paddy fields<br>
I have seen spread all over sweet smiles.<br>

Ah, what beauty, what shades,<br>
What an affection, and what tenderness!<br>
What a quilt have you spread<br>
At the feet of banyan trees<br>
And along the banks of rivers!<br>

Oh mother mine, words from your lips<br>
Are like nectar to my ears.<br>
Ah, what a thrill!<br>

If sadness, O mother mine,<br>
Casts a gloom on your face,<br>
My eyes are filled with tears!<br>
Golden Bengal,<br>
I love you.<br>

