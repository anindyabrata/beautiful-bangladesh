<h1 style="color:red;">Ratargul</h1>
 <img src="assets/images/ratargul.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Ratargul Swamp Forest is a fresh water marshland forest located close to river Goain, in Gowainghat Upazila of Sylhet District. This forest is engulfed with freshwater in most of the seasons. This evergreen forest gets over flooded during the rainy season. Different local fish are available here. It’s the terrain of different creatures’ like snakes, monkeys, lizards, insects & birds.

You may also see vulture and Eagle. Some parts of this forest are very small, so boat ride would be tough. However, it is the only swamp forest located in Bangladesh and one of the few freshwater swamp forests in the world. The forest is naturally conserved under the Department of Forestry, Government of Bangladesh.

This evergreen forest is situated by the river Goain and linked with the canal named Chengir Khal. Most of the trees grow here are Pongamia pinnata (Koroch tree). The forest goes under 20-30 feet water in the rainy season. Rest of the year the water level is about 10 feet deep.</p>
          
    
          
 <img src="assets/images/ratargulclimate.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Climate</h3>
          <p align="justify">Tropical air from the north-west of Sylhet causes heavy rainfall. According to Sylhet Weather Centre, average rainfall is 4162 millimetre per year. Highest rainfall occurs in July which is 1250 millimetre. On the other hand, December is the driest season with 74% relative density which jumps up to more than 90% in July–August. The forest is linked with Gowain river through a lake namely chengir khal. In the rainy season, too much water from India enter into the lake through Gowain river and the forest become flooded. This situation prevails from May to early October and the temperature remains 32 degree Celsius which drops to 12 degree Celsius in January. In the rainy season, trees remain 10 ft (somewhere even 15–20 ft) under water but the forest become dry in winter season.</p>
          

    
          
 <img src="assets/images/bandarban.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Vegetation</h3>
          <p align="justify">73 species of plants could be found in the forest till now. 80 percent of the forest area is covered with umbrella of the trees

Two layer of plants can be seen in the swamp forest. The upper layer consists of trees and the lower one consists of intense Schumannianthus dichotomus. The canopy of the plants spreads up to 15 meters of height.

Though the forest is natural, the Forestry Department of Bangladesh has planted some watery plants like Calamus tenuis, Burflower Tree, Barringtonia acutangula, Calamus tenuis. Banyan Tree is very common in the forest. Besides that, Barringtonia acutangula, Milletia Pinnata, Sacred Garlic Pear or Temple Plant, Alstonia Scholaris can also be seen.</p>
          
 
    
          
 <img src="assets/images/ratargulanimals.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Wildlife</h3>
          <p align="justify">Snake and worm snake can widely be seen in this water-drown forest. Mongoose can be seen in dry season. Monkey and Water Monitor also resides in the forest. Heron, Egret, Kingfisher, Parrot, Bulbul, Swan, Dove, Water fowl, Eagle and Kite Bird are some of the birds of the swamp forest. Cotton Pygmy Goose and other Migratory birds and Vulture visit the forest in winter. Name of some local fishes available in the forest are Batasio, Rita, Pabda, Rohu etc.</p>
          

          
<img src="assets/images/ratargulvisit.jpg" width="800px" height="400px" /> <br> <br>
<h3 style="color:green;">How to Go</h3>
  <p align="justify">It is approximately 18.7 km away from Sylhet. You may take bus or CNG auto rickshaw and head towards Saklutikor road. Take rickshaw to reach there.
</p>
          
      
