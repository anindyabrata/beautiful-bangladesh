 <h1 style="color:red;">Sundarban</h1>
 <img src="assets/images/sundarbans.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Satkhira region is mainly famous for the Sundarbans. Sundarbans is most large single block of tidal mangrove forest in the world. It lies between the vast Bay of Bengal Ocean and rivers of the Ganges and the Brahmaputra, it spreads across Bangladesh and West Bengal in India. The waters of these rivers mix with the ocean water and so in this part of their flow the rivers are brackish or saline. Water covers the forest-islands twice a day during high tide.

It is considered a World Heritage site by UNESCO. The forest is mostly popular for incredible plants and wildlife. Trees having roots can breathe directly from the air. There are numerous species and it is mostly famous for Royal Bengal tiger. The forest safeguards people of north from cyclones and tsunami waves. Previously, the islanders were allowed to go into the forest to collect firewood, honey, crab and fish but now these activities have been banned by the governments of both Bangladesh and West Bengal.

</p>
          
         
 <img src="assets/images/sundarbantigers.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Wildlife</h3>
          <p align="justify">The Sundarbans provides a unique ecosystem and a rich wildlife habitat. According to the 2015 tiger census in Bangladesh, and the 2011 tiger census in India, the Sundarbans have about 180 tigers (106 in Bangladesh and 74 in India). Earlier estimates, based on counting unique pugmarks, were much higher. The more recent counts have used camera traps, an improved methodology that yields more accurate results. Tiger attacks are frequent in the Sundarbans. Between 0 and 50 people are killed each year.

There is much more wildlife here than just the endangered Bengal tiger (Panthera tigris tigris). Most importantly, mangroves are a transition from the marine to freshwater and terrestrial systems, and provide critical habitat for numerous species of small fish, crabs, shrimps and other crustaceans that adapt to feed and shelter, and reproduce among the tangled mass of roots, known as pneumatophores, which grow upward from the anaerobic mud to get the supply of oxygen. Fishing cats, macaques, wild boars, common grey mongooses, foxes, jungle cats, flying foxes, pangolins, and spotted deer are also found in abundance in the Sundarbans.

</p>
 <img src="assets/images/sundarbandeer.jpg" width="800px" height="400px" /> <br> <br>
          
 <img src="assets/images/sundarbantrees.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Vegetation</h3>
          <p align="justify">A total 245 genera and 334 plant species were recorded by David Prain in 1903. While most of the mangroves in other parts of the world are characterised by members of the Rhizophoraceae, Avicenneaceae or Combretaceae, the mangroves of Bangladesh are dominated by the Malvaceae and Euphorbiaceae. Dominant flora includes:

The Sundarbans flora is characterised by the abundance of sundari (Heritiera fomes), gewa (Excoecaria agallocha), goran (Ceriops decandra) and keora (Sonneratia apetala) all of which occur prominently throughout the area. The characteristic tree of the forest is the sundari (Heritiera littoralis), from which the name of the forest had probably been derived. It yields a hard wood, used for building houses and making boats, furniture and other things. New forest accretions is often conspicuously dominated by keora (Sonneratia apetala) and tidal forests. It is an indicator species for newly accreted mudbanks and is an important species for wildlife, especially spotted deer (Axis axis). There is abundance of dhundul or passur (Xylocarpus granatum) and kankra (Bruguiera gymnorrhiza) though distribution is discontinuous. Among palms, Poresia coaractata, Myriostachya wightiana and golpata (Nypa fruticans), and among grasses spear grass (Imperata cylindrica) and khagra (Phragmites karka) are well distributed.</p>
       
          
 <img src="assets/images/sundarbanpeople.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Habitation</h3>
          <p align="justify">The Sundarbans has a population of over 4 million but much of it is mostly free of permanent human habitation. Despite human habitations and a century of economic exploitation of the forest well into the late 1940s, the Sundarbans retained a forest closure of about 70% according to the Overseas Development Administration (ODA) of the United Kingdom in 1979.</p>
       
    
<img src="assets/images/sundarbanvisit.jpg" width="800px" height="400px" /> <br> <br>
<h3 style="color:green;">How to Go</h3>
  <p align="justify">It is (mainly) located in Satkhira district of Khulna divison. It is 66.4km away from Satkhira. After reaching in Satkhira, you can use local transports or private cars to reach Sundarbans.
</p>

