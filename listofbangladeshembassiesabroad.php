 <h1 style="color:red;">Sundarban</h1>
 <img src="assets/images/sundarbans.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Satkhira region is mainly famous for the Sundarbans. Sundarbans is most large single block of tidal mangrove forest in the world. It lies between the vast Bay of Bengal Ocean and rivers of the Ganges and the Brahmaputra, it spreads across Bangladesh and West Bengal in India. The waters of these rivers mix with the ocean water and so in this part of their flow the rivers are brackish or saline. Water covers the forest-islands twice a day during high tide.

It is considered a World Heritage site by UNESCO. The forest is mostly popular for incredible plants and wildlife. Trees having roots can breathe directly from the air. There are numerous species and it is mostly famous for Royal Bengal tiger. The forest safeguards people of north from cyclones and tsunami waves. Previously, the islanders were allowed to go into the forest to collect firewood, honey, crab and fish but now these activities have been banned by the governments of both Bangladesh and West Bengal.

</p>
      
 
          
              
