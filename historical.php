 <h1 style="color:red;">Shaat Gombuj Mosque</h1>
 <img src="assets/images/shat.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">The Sixty Dome Mosque (more commonly known as Shait Gambuj Mosque or Saith Gunbad Masjid),a UNESCO World Heritage Site, is a mosque in Bangladesh, the largest in that country from the Sultanate period. It has been described as "the most impressive Muslim monuments in the whole of the Indian subcontinent.

In mid-15th century, a Muslim colony was founded in the unfriendly mangrove forest of the Sundarbans near the coastline in the Bagerhat District by a saint-General, named Khan Jahan Ali. He preached in an affluent city during the reign of Sultan Nasiruddin Mahmud Shah, then known as 'Khalifalabad'. Khan Jahan adorned this city with more than a dozen mosques, the spectacular ruins of which are focused around the most imposing and largest multidomed mosques in Bangladesh, known as the Shait-Gumbad Masjid (160'×108'). 

The construction of the mosque was started in 1442 and it was completed in 1459.The mosque was used for prayer purposes. It was also used as a madrasha and assembly hall.</p>
          
          
 
 <h3 style="color:green;">How to Go</h3>
  <p align="justify">It is located in Bagerghat district of Khulna divison. A cycle-rickshaw from the bus stand to the Shat Gombuj Mosque should run around Tk 10. Unless you’re in a hurry, you may want to just walk to the sights from the bus stand. Head west along the main road, a fairly nice walk save for the speedy honking buses. The first sight you’ll come to is the dargah; start asking around for it after about 5km or so. Shat Gombuj mosque is another 2 km or so west along the main road from the dargah turn off.</p>

