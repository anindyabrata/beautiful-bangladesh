 <h1 style="color:red;">Shahid Minar</h1>
 <img src="assets/images/shahidminar.jpg" width="800px" height="400px" /> <br> <br>
<h3 style="color:green;">Description</h3>
 <p align="justify">The Shaheed Minar is a national monument in Dhaka, Bangladesh, established to commemorate those killed during the Language Movement demonstrations of 1952.On February 21, 1952, dozens of students and political activists were killed when the Pakistani police force opened fire on Bengali protesters who were demanding equal status to their native tongue, Bengali. The massacre occurred near Dhaka Medical College and Ramna Park in Dhaka. A makeshift monument was erected on February 23 by students of University of Dhaka and other educational institutions, but soon demolished on February 26 by the Pakistani police force.The Language Movement gained momentum and after a long struggle, Bengali was given equal status as Urdu. To commemorate the dead, the Shaheed Minar was designed and built by Hamidur Rahman, a Bangladeshi sculptor. The monument stood until the Bangladesh Liberation War in 1971, when it was demolished completely during Operation Searchlight. After Bangladesh gained independence, it was rebuilt.At present, all national, mourning, cultural and other activities occurred each year regarding 21 February is centered around the Shaheed Minar.
</p>

<h3 style="color:green;">How to Go</h3>
  <p align="justify">The Shaheed Minar is situated near Dhaka Medical College Hospital and in the Dhaka University area. It is adjacent to the Mathematics Department of Dhaka University.
</p>
 
