 <h1 style="color:red;">Sonargaon Upazila</h1>
 <img src="assets/images/sonargaon.jpg" width="800px" height="400px" /> <br> <br>
          <h3 style="color:green;">Description</h3>
          <p align="justify">Sonargaon was a historic administrative, commercial and maritime center in Bengal. Situated in the center of the Ganges delta, it was the seat of the medieval Muslim rulers and governors of eastern Bengal. Sonargaon was described by numerous historic travelers, including Ibn Battuta, Ma Huan, Niccolò de’ Conti and Ralph Fitch as a thriving center of trade and commerce. It served as the capital of Sultan Fakhruddin Mubarak Shah, Isa Khan and the Baro-Bhuyan Confederacy.

The area is located near the modern industrial river port of Narayanganj in Bangladesh. Today, the name Sonargaon survives as the Sonargaon Upazila (Sonargaon Subregion) in the region.</p>
          
          
 
 <h3 style="color:green;">How to Go</h3>
  <p align="justify">Frequent bus services to Sonargaon operate from Gulistan, Saidabad and other bus stands in Dhaka. Tickets may be bought on roadside counters. The price of the ticket from Gulistan bus stand is around 35 BDT, and the ride may take about 40 minutes, depending on the unpredictable traffic. Mention your destination as Mograpara. You can reach that place using bus from Dhaka (Gulistan). This will take you around 1 hour to reach at the place. You have to get down from the bus at Mograpara Crossing.From the crossing, you have to take a rickshaw, and have to tell the puller to drop you at Sonargaon. This will require 20 taka for the lift.</p>

