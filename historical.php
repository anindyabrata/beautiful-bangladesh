National Martyrs Memorial is situated in Nabinagar, Savar approximately 35 km from Dhaka. The memorial designed by architect Moinul Hossain is dedicated to the sacred memory of the millions of unknown martyrs of the war of liberation. This Martyrs Memorial is a symbol of Bengali nationalism. It is really a scenic beauty of 108 acre of land. The top of this monument is 150 ft. high, which consists other 07 triangular monuments.</p>
          
          
 
